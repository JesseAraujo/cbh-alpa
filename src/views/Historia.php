        <p>
            Instalado em 17 de maio de 1996, com o intuito de gerenciar os recursos hídricos na bacia, de modo a conservar, preservar e recuperar seus recursos ao longo dos anos de sua atuação.
            Em conformidade com a Deliberação CRH nº146/2012, o plano de bacia é um instrumento de gestão que define diretrizes gerais para o desenvolvimento local regional de ações de
            planejamento na UGRHI, através do estabelecimento de metas e ações vinculadas à garantia da qualidade e quantidade das águas de acordo com os recursos financeiros existentes.
            As metas e ações são sustentadas através de módulos implementados como o diagnóstico, que permite obter a situação geral da bacia, o prognóstico, que avalia a evolução da situação
            dos recursos hídricos, confeccionando cenários, por meio de variáveis pertinentes às análises, como, demandas, disponibilidades, e por fim o Plano de Ação contempla um conjunto de metas,
            ações e investimentos, que permite que a realidade projetada seja alcançada, através do uso dos indicadores para acompanhar a sua vigência.
        </p>

        <p>
            Anualmente o CBH-ALPA delibera sobre a aplicação dos recursos financeiros proveniente do Fundo Estadual de Recursos Hídricos (FEHIDRO), o qual constitui o braço financeiro do
            Sistema de Gerenciamento de Recursos Hídricos do Estado de São Paulo (SIGRH).
        </p>

        <p>
            Conforme a divisão hidrográfica do Estado de São Paulo estabelecida pela Lei 9.034 de 27 de dezembro de 1994, a UGRHI-14 corresponde à Bacia Hidrográfica Alto
            Paranapanema e está localizada no sudoeste do Estado de São Paulo, sendo uma das seis Unidades de Gestão dos Recursos Hídricos pertencentes à Bacia do Paranapanema,
            cujo domínio é da União. A bacia limita-se ao norte com a UGRHI – 17 Médio Paranapanema, ao sul com a UGRHI-11 Ribeira de Iguape/Litoral Sul, a leste com a UGRHI-10 Sorocaba
            Médio Tietê e a oeste com a vertente paranaense da Bacia do Rio Paranapanema, que corresponde à bacia dos rios Cinzas, Itararé, Paranapanema I e II, os quais integram o
            comitê da bacia hidrográfica do Norte Pioneiro. A UGRHI-14 abrange 34 municípios paulistas com sede na bacia, incluindo as áreas de alguns outros municípios como Sarapuí,
            Avaré, Cerqueira César, Itatinga, Pardinho, Bofete, Piedade, Tapiraí, Apiaí e Chavantes.
        </p>

        <p>
            A UGRHI-14 é a maior do Estado de São Paulo, com 22.738,2 km² de área territorial, representando 21,3% do território da Bacia do Rio Paranapanema.
        </p>

        <p>
            A UGRHI-14 (Alto Paranapanema) é considerada como uma Bacia de Conservação Ambiental, contanto com uma variedade no âmbito da vegetação que ocupa o seu território,
            como remanescentes de Mata Atlântica, sobretudo, na porção meridional da unidade hidrográfica, na região da Serra de Paranapiacaba, junto ao limite com a UGRHI 11, onde
            estão localizados a APA Serra do Mar, os parques estaduais Carlos Botelho e Intervales. Aproximadamente 15% de seu território abrangem estações ecológicas e as APAs
            Botucatu e Tejupá. Nessas áreas ocorrem as florestas ombrófila densa e mista, com a presença da Araucária Angustifólia. Já a porção centro-ocidental da unidade é ocupada
            por vegetações de transição, floresta estacional semidecidual e cerrado, sendo o último apresentando um avanço significativo do desmatamento, devido à elevada demanda de
            uso da água para a irrigação de culturas como soja, trigo, milho, feijão, cana-de-açúcar, silvicultura e algodão, que constituem parte do uso do solo da região.
        </p>

        <p>
            Destacam-se como unidades de conservação na UGRHI-143 : APA Corumbataí, Botucatu e Tejupá, APA da Serra do Mar, EE de Angatuba, EE de Xitué, EE de Paranapanema, EE de Itapeva,
            EE de Itaberá, FE de Angatuba, FE de Manduri, FE de Paranapanema, FE de Piraju, FN Capão Bonito, PE Intervales, PE Carlos Botelho, RPPN Fazenda Horii, RPPN Vale do Corisco.
        </p>

        <p>
            A UGRHI-14 abrange 34 municípios paulistas com sede na bacia, incluindo as áreas de alguns outros municípios como Sarapuí, Avaré, Cerqueira César, Itatinga, Pardinho, Bofete,
            Piedade, Tapiraí, Apiaí e Chavantes.
        </p>

        <p>
            A UGRHI-14 apresenta uma economia maioritariamente agrícola, com indústrias de papel e celulose, mineração de calcário e plantio e processamento de madeiras de reflorestamento.
            Possui também grande potencial turístico, principalmente nas áreas das Represas de Jurumirim e Chavantes.
        </p>

        <p>
            A agricultura irrigada (por pivôs) está presente na maior parte do território da UGRHI14 Alto Paranapanema, sobretudo, nos municípios de Itaí, Itapeva, Paranapanema, Buri,
            Itaberá e Itapetininga, sendo as demandas superficiais maiores nessa região.
        </p>

        <p>
            Os municípios de Ribeirão Branco e Itapeva são os maiores produtores de tomate, e nas demais regiões destacam-se com expressividade, as lavouras de feijão, milho e soja.
            Assim, o uso do solo é caracterizado pela presença de atividades agrícolas em maior expressividade.
        </p>



